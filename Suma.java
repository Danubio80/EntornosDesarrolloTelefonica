package calculadora_c;


public class Suma {
	static double sumaR;
	static int sumaE;
	
	public static double sumar(double n1,double n2){
		
		return sumaR = n1 + n2;
		
	}
	
	public static int sumar(int n1,int n2){
		return sumaE = n1 + n2;
		
	}
	
	public static double sumar(double n1,double n2,double n3){
		return sumaR = n1 + n2 + n3;
	}
	
	public static double sumarAcumulado(double n1){
		return sumaR=sumaR + n1;
		
		
		
	}

}
